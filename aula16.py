entrada = input('Você quer entrar no sistema? ')

if entrada == 'Sim':
    print('Você entrou no sistema')

elif entrada == 'Não':
    print('Você não entrou no sistema')

elif entrada == 'Entrar devagarinho':
    print('Você entrou e ninguém te viu')

else:
    print('Você deve digitar "Entrar" ou "Não"')