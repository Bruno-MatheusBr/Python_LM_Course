# Variáveis são usadas para salvar algo na memória do computador.
# PEP8: inicie variáveis com letras minúsculas.
# Pode usar números e underline _.
# O sinal de = é o operador de atribuição.
# Ele é usado pra atribuir um valor a um nome (variável).
# Uso: nome_variável = expressão

nome = "Bruno"
sobrenome = "Domingues"
idade = 38
anoDeNascimento = 2023 - idade
maiorDeIdade = idade >= 18
alturaMetros = 1.82

print("Nome: ", nome)
print("Sobrenome: ", sobrenome)
print("Ano de nascimento: ", anoDeNascimento)
print("É maior de idade? ", maiorDeIdade)
print("Altura em metros: ", alturaMetros)
